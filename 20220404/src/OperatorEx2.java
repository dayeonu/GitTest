
public class OperatorEx2 {

	public static void main(String[] args) {
		
		int i = 5, j = 0;
	
		j = i++;
		System.out.println(i);
		System.out.println(j);
		System.out.println(i+j);
		
System.out.println("---------------------------------------");	
	
		i = 5;
		j = 0;
 	
		j = ++i;
		System.out.println(i);
		System.out.println(j);
		System.out.println(i+j);
		
	}
}
